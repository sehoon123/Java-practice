class Main {
    public static int sum(int a, int b) {
        return a + b;
    }
    public static int minus(int a, int b) {
        return a - b;
    }
    public static void main(String[] args) {
        System.out.println("Hello world!");
        System.out.println(sum(1,2));
        System.out.println(minus(1,2));
    }
}